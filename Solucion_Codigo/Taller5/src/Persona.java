
public class Persona {

    String nombre;
    int edad;
    String ocupacion;
    String rol;
    boolean colabora;

    public Persona(String nombre, int edad, String ocupacion, String rol, boolean colabora) {
        this.nombre = nombre;
        this.edad = edad;
        this.ocupacion = ocupacion;
        this.rol = rol;
        this.colabora = colabora;
    }
}
