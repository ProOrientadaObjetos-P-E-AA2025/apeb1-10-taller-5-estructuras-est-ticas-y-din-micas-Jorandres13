
public class EventoConflicto {
    String nombre;
    String fecha;
    String ubicacion;
    String tipo; 
    String descripcion;
    int bajas;

    public EventoConflicto(String nombre, String fecha, String ubicacion, String tipo, String descripcion, int bajas) {
        this.nombre = nombre;
        this.fecha = fecha;
        this.ubicacion = ubicacion;
        this.tipo = tipo;
        this.descripcion = descripcion;
        this.bajas = bajas;
    }
}

